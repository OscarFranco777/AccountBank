    private void jltsClienteValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jltsClienteValueChanged
        if (jltsCliente.getSelectedIndex() > -1) {
            Cliente C1 = new Cliente();
            C1 = (Cliente) pr.Lc.get(jltsCliente.getSelectedIndex());
            jtfCodigo.setText(String.valueOf(C1.Codigo));
            jtfNombre.setText(C1.Nombre);
            jtfNoID.setText(C1.Id);
            jtfDireccion.setText(C1.Direccion);
            jtfTelefono.setText(C1.Telefono);
            jtfCorreo.setText(C1.Correo);
            if (C1.Sexo) {
                jrbFemenino.setSelected(true);
            } else {
                jrbMasculino.setSelected(true);
            }            
            jckbCasa.setSelected(false);            
            jckbVehiculo.setSelected(false);
            jckbOtros.setSelected(false);
        }
    }//GEN-LAST:event_jltsClienteValueChanged

