    private void jbtAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtAgregarActionPerformed
        int ind = -1;
        Cliente C1 = new Cliente(Integer.parseInt(jtfCodigo.getText()),
                jtfNombre.getText(), jtfNoID.getText(),
                jtfDireccion.getText(), jtfTelefono.getText(),
                jtfCorreo.getText(), jrbFemenino.isSelected(),
                jckbCasa.isSelected(), jckbVehiculo.isSelected(),
                jckbOtros.isSelected());
        ind = C1.Buscar(pr.Lc);
        if (ind == -1) {
            pr.Lc.add(pr.Lc.size(), C1);
            Ls.add(Ls.getSize(), C1.Codigo + "-" + C1.Nombre);
            Limpiar();
            JOptionPane.showMessageDialog(null, "Se Agrego un nuevo elemento..!");
        } else {
            JOptionPane.showMessageDialog(null, "Elemento ya Existe");
        }
    }//GEN-LAST:event_jbtAgregarActionPerformed
    
