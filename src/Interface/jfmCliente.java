    private void jbtEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtEliminarActionPerformed
        int ind = -1;
        Cliente C1 = new Cliente(Integer.parseInt(jtfCodigo.getText()),
                jtfNombre.getText(), jtfNoID.getText(),
                jtfDireccion.getText(), jtfTelefono.getText(),
                jtfCorreo.getText(), jrbFemenino.isSelected(),
                jckbCasa.isSelected(), jckbVehiculo.isSelected(),
                jckbOtros.isSelected());
        ind = C1.Buscar(pr.Lc);
        if (ind != -1) {
            pr.Lc.remove(ind);
            Ls.remove(ind);
            Limpiar();
            JOptionPane.showMessageDialog(null, "Se elimino un nuevo elemento..!");
        } else {
            JOptionPane.showMessageDialog(null, "Elemento no Existe");
        }
    }//GEN-LAST:event_jbtEliminarActionPerformed

    private void jbtEditar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtEditar1ActionPerformed
        int ind = -1;
        Cliente C1 = new Cliente(Integer.parseInt(jtfCodigo.getText()),
                jtfNombre.getText(), jtfNoID.getText(),
                jtfDireccion.getText(), jtfTelefono.getText(),
                jtfCorreo.getText(), jrbFemenino.isSelected(),
                jckbCasa.isSelected(), jckbVehiculo.isSelected(),
                jckbOtros.isSelected());
        ind = C1.Buscar(pr.Lc);
        if (ind != -1) {
            pr.Lc.set(ind, C1);
            Ls.add(ind, C1.Codigo + "-" + C1.Nombre);
            Limpiar();
            JOptionPane.showMessageDialog(null, "Se edito un nuevo elemento..!");
        } else {
            JOptionPane.showMessageDialog(null, "Elemento no Existe");
        }
    }//GEN-LAST:event_jbtEditar1ActionPerformed

